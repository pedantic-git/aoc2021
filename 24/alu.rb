#!/usr/bin/env ruby

INSTRUCTIONS = DATA.readlines

class ALU
  
  attr_accessor :reg, :input
  def initialize(input=[])
    @reg = {'w' => 0, 'x' => 0, 'y' => 0, 'z' => 0}
    @input = input
    INSTRUCTIONS.each(&method(:step!))
  end
  
  def step!(instruction)
    case instruction
    when /inp (\w)/
      reg[$1] = input.shift
    when /add (\w) (.*)/
      reg[$1] += reg[$2] || $2.to_i
    when /mul (\w) (.*)/
      reg[$1] *= reg[$2] || $2.to_i
    when /div (\w) (.*)/
      reg[$1] /= reg[$2] || $2.to_i
    when /mod (\w) (.*)/
      reg[$1] %= reg[$2] || $2.to_i
    when /eql (\w) (.*)/
      reg[$1] = (reg[$1] == (reg[$2] || $2.to_i) ? 1:0)
    end
  end
  
  def pass?
    reg["z"] == 0
  end
  
end

serial = ARGV[0]&.split('')&.map(&:to_i) or fail "Please supply serial"
alu = ALU.new(serial)
p alu.pass?

